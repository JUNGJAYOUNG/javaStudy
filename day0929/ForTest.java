import java.util.Scanner;

class ForTest
{
	public static void main(String[] args) 
	{
		Scanner input = new Scanner(System.in);
		
		int i=1;
		int sum=0; //합계 누적변수 0으로 초기화
		int n;
		System.out.print("1부터 어떤수까지 더할까요?");
		n= input.nextInt();

		for(i=1 ; i<=n ; i++){
			sum += i;
		}

		/*
		sum += i;
		i++;
		sum += i;
		i++;
		sum += i;
		*/
		System.out.println("1부터 "+n+"까지의 합은"+sum+"입니다.");

	}
}
